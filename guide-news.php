<p>Salutations mes chers toads, ici Maxhu qui vous écris </p>

<p>Il y a quelques temps, un guide sur les news a été crée par Toaddle et Layton qui ont voulu apporter leurs conseils pour faire de bonnes news. <br />
Sauf que, sans être méchant avec eux, ce ne sont que des rédacteurs occasionnels. J'ai décidé de refaire ce guide, en apportant ce que j'ai appris en tant que rédacteur officiel sur Planète Toad. Je reprends ce que Toaddle et Layton ont dit mais je vais plus pousser leur idées. <br />
Alors voici donc une mise à jour du guide des newsers !</p>

<p><h2>1 - Les titres aguicheurs et les pièges à clics</h2></p>

<p>Déjà, un titre aguicheur ne veut pas totalement dire piège à clics (ou putaclic). <br />
Un titre aguicheur nous donne envie de cliquer pour voir le contenu qui existe réellement dedans. <br />
Un piège à clics est une technique utilisée par des sites ou des vidéos pour inciter à clique pour juste créer le succès <em>(ex. on vous promet une bagarre, dans une vidéo, qui sera présente à même pas 1/3 de la vidéo (voir même jamais).)</em><br />
Nous autoriserons les news ayant un titre aguicheur, tant que vous avez promis ce que vous allez annoncé dans le titre <em>(ex. "Shigeru Miyamoto <br />
nous présente ses secrets du développement de The Legend Of Zelda : Breath Of The Wild" )</em>. Par contre, on ne va pas accepté le piège à clics. <br />
Donc, il est interdit d'écrire la news tout en majuscule avec plusieurs points d'exclamations <em>(ex. "OMG SHIGERU MIYAMOTO NOUS RACONTE LA SCENE <br />
DE SEXE COUPEE ENTRE LINK ET ZELDA DANS LE DERNIER ZELDA !!!!" )</em> et des miniatures assez osées <em>(ex. Femme dénudé, bagarre...)</em>.</p>

<p><h2>2 - L'état votre news </h2></p>

<p>Quand vous allez écrire votre news, quel type de news faîtes-vous ?</p>

<p>- Est-ce que vous allez montrer une vidéo ? Dans ce cas, ajoutez dans votre titre "[Vidéo]" et mettez la vidéo dans la news en ajoutant quelque chose, comme expliquer ce qu'il y a dans la vidéo et quand ça va sortir ! </p>

<p>ex. <em>"Depuis l'annonce de la Nintendo Switch, on peut voir qu'un portage de Minecraft est annoncé ! Récemment, Nintendo a sorti une bande-annonce <br />
de lancement. Pour ce portage, pas grand chose. Il y a toujours le pack "Super Mario Mash-Up Pack" inclus dedans.</p>

<p><iframe width="560" height="315" src="https://www.youtube.com/embed/HoBCXFEH2N8" frameborder="0" allowfullscreen></iframe></p>

<p>Minecraft : Nintendo Switch Edition est disponible dés maintenant sur le Nintendo eShop"</em></p>

<p>- Est-ce que cette news sera courte ? Dans ce cas, mettez "[News Brève]". Vous serez plus excusés pour avoir fait une news courte !</p>

<p>- Est-ce que cette news contiendra des éléments clés d'un jeu qui peut gâcher ceux qui n'ont pas joué ? Dans ce cas, mettez "[Spoiler]" ou<br />
"[Spoiler Alert]" dans votre titre et/ou ajoutez la fonction "spoiler" dans votre news. Vous ne serez pas punis car vous avez, par exemple, gâché la fin du dernier <em>The Legend Of Zelda</em> mais il faut savoir que tout le monde n'a pas ce jeu donc faites très attention !</p>

<p><h2>3 - Les choses à ne pas faire[/s] </h2></p>

<p>- Il est interdit de faire un "copier-coller" d'une news déjà existante, dans d'autres sites. Pour ça, <br />
<a src=http://www.planete-toad.fr/news-2294-plus-de-precision-sur-minecraft-version-wii-u.html] > je pointe une news en particulier !</a><br />
Qu'est-ce qui ne va pas dans cette news ? Quelle est la grosse erreur de cette news ? Eh bien, il s'agit d'un "copier-coller" de<br />
<a src="http://fr-minecraft.net/news-minecraft-1639-les-specificites-de-minecraft-wii-u-editions.html]" >ça !</a><br />
Celui qui a fait ce c/c n'a même pas cité que cette news vient de fr-minecraft.net ! Vous ne serez peut-être pas puni par le staff pour<br />
avoir fait ça mais n'abusez pas le c/c !</p>

<p>- Signer sa news est stupide. Le lecteur s'en moque de savoir qui a écrit ça. Il n'est pas venu savoir qui a écrit le plus de news sur le dernier <em>Nintendo Direct</em>. En plus de ça, le nom du rédacteur est affiché avec l'heure et la date indiquées, raison de plus de ne pas le faire...</p>

<p>- Ne rédigez pas n'importe quoi ! <br />
On en a rien contre les news présentant les exclusivités étrangères par Nintendo <em>(surtout que Nintendo est une entreprise japonaise, évidemment, donc pas de soucis)</em> mais est-ce que ça vaut vraiment le coup d'écrire une news sur une convention aux Etats-Unis qui distribue les Pokémons rares ? Nous sommes, pour la majorité, mineurs. Je ne pense pas que vous allez convaincre vos parents de partir au Japon, juste pour avoir votre Mewtwo. Bon, si vous y arrivez, tant mieux... mais voilà quoi. <br />
Je dis ça, au cas où, nous sommes dans un fan-site Nintendo, principalement sur Mario, donc une news sur un jeu de la concurrence <em>(Playstation <br />
ou Xbox)</em> n'a rien à faire là. Si vous voulez parler du nouveau jeu PS4, parlez-en dans un topic spécialisé.<br />
C'est pareil pour de grosses infos qui passent souvent dans les journaux ou dans les JTs, même si c'est important pour le monde entier, ça n'a évidemment rien à faire dans les news de Planète Toad. Parlez-en dans un topic spécialisé, si vous voulez parler de cette info importante.</p>

<p><h2>5 - Les sources</h2></p>

<p>Vérifez bien que votre news vient du site officiel de <em>Nintendo</em> ou d'un autre site réputé. Ne tombez pas dans le panneau en citant des sites pour faire le buzz comme <em>BuzzFeed</em> qui ne sont pas toujours fiables. </p>

<p><h2>6 - Les détails des news</h2></p>

<p>- Pour les BBcodes, il faut utiliser le gras pour les mots-clés importants, l'italique pour les noms et les moments pas très importants, le surligneur pour les titres et les citations pour citer un passage d'un article ou d'un interview. <br />
Pour mettre des liens-textes quand vous parler d'un site ou d'une vidéo, il faut faire ça : [url=L'URLAMETTRE]LETITREAMETTRE[./url] (sans le point "." )</p>

<p>- Pour les images, ne prenez pas directement celles des autres sites. Ces images peuvent être de mauvaise qualité, trop grande ou trop petite. Nous vous conseillons de privilégier les images en PNG et d'utiliser des hébergeurs d'images (<em>MegaPict, NoelShack</em>...)</p>

<p>- Pour insérer des tweets, vous devez faire un clique sur les trois petits points à côté du bouton like et retweet de <em>Twitter</em> et<br />
vous cliquez sur "Intégrer un tweet".</p>

<p>- Pour insérer les vidéos YouTube, cliquez sur "Partager", puis "Intégrer" et vous copiez le code. </p>

<p>- Pour <em><b>l'italique en gras</b></em>, c'est inutile car <b>le gras sert à donner de l'importance</b> et <em>l'italique sert à faire le contraire</em>. Si vous voulez faire un truc joli, [rouge]les[/rouge] [orange]couleurs[/orange] [vert]sont[/vert] [bleu]à[/bleu] [rose]votre[/rose] [gris]disposition[/gris]. </p>

<p><h2>7 - La catégorie des news</h2></p>

<p>Quand vous publiez des news, vérifiez bien que c'est la bonne catégorie. <br />
<em>(ex. Si vous voulez parler de Super Mario Oddyssey, ce serait dans<br />
la catégorie "Switch", normalement)</em></p>

<p><h2>8 - La relecture</h2></p>

<p>La relecture est très importante, pour les petites fautes de français. On ne vous demande pas d'être le nouveau Victor Hugo mais il faut que votre<br />
orthographe et votre gammaire soient corrects. </p>

<p><h2>9 - Les miniatures</h2></p>

<p>Les news les plus récentes sont affichés à l'accueil du site avec des miniatures <em>(un peu comme dans les vidéos YouTube)</em>. Si vous voulez envoyer la miniature mais que vous n'êtes pas du staff, n'hésitez pas à l'envoyer à Toaddle, à Champoad ou à moi pour la mettre. Attention,<br />
<b>votre miniature doit faire 230x170px et 200Ko</b> sinon ça ne marche pas !</p>

<p><h2>10 - Les chroniques</h2></p>

<p>Certains membres veulent faire une chronique à tout prix !<br />
Avant, j'avais fais une chronique qui s’appelait "Le Vrac de Nintendo" traitant de l'actualité <em>Nintendo</em>. Plusieurs problèmes ont été crée avec cette chronique. <br />
D'abord, j'étais le premier membre à faire ce concept et plusieurs autres tentent de faire de même alors que ça tiendrait en news et, enfin, depuis que j'ai arrêté cette chronique, j'ai pris du recul avec ce concept et je me suis dis que ce genre de chronique ne sert à que dalle, vu qu'il y a des news. <br />
En plus, à la base, le Vrac de Nintendo n'était pas censé être une chronique, c'était une technique de flemmard pour faire trois news en un mais le staff a pris ça comme une chronique...<br />
Bref, si vous voulez faire une chronique, <b>je vous déconseille fortement de faire une revue d'actualité en générale de deux ou trois news en <br />
un</b> ! Vous pouvez faire des vidéos du style Journal Télévisé ou s'attarder sur quelque chose en particulier <em>(ex. l'eShop News de Migmag qui se penche plus sur le Nintendo eShop)</em>. Pour faire une chronique, <b>elle faut qu'elle soit assez originale et axée sur les jeux vidéos ! </b></p>

